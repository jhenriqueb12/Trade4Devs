let alunos = ["joão", "José", "Maria", "Carla"]

// Adicionar um item no array por ultimo
console.log(alunos.push("Paulo"))

// Adicionar um item no array em primeiro
console.log(alunos.unshift("Carlos"));

// Mostrar apena o primeiro item do array
console.log(alunos.shift());

// Remover um item do array
console.log(alunos.pop());